$(document).ready(function () {

    var getCountPage = function (post) {
        return Math.ceil(post.length / pageData.maxPost);
    };

    var getNumberPage = function (post) {
        var currentPage = +getUrlVars()["page"];
        if (currentPage < 1 || !currentPage || currentPage > getCountPage(post)) {
            currentPage = 1;
        }
        return currentPage;
    };

    var getIdOpenPost = function () {
        return +getUrlVars()["post"];
    };

    var getUrlVars = function () {
        var vars = [], hash;
        var hashes = window.location.search.substr(1).split('&');
        for (var i = 0; i < hashes.length; i++) {
            hash = hashes[i].split('=');
            vars.push(hash[0]);
            vars[hash[0]] = hash[1];
        }
        return vars;
    };

    var data = {
        "full": false,
        "post": []
    };

    var pageData = {
        "page": 1,
        "pages": [],
        "maxPost": 5,
        "visible": true,
        "first": false,
        "last": false,
        "prev": function () {
            return function (text, render) {
                return +render(text) - 1;
            };
        },
        "next": function () {
            return function (text, render) {
                return +render(text) + 1;
            };
        }
    };

    var createContent = function (post) {
        pageData.countPage = getCountPage(post);
        if (pageData.countPage < 2) {
            pageData.visible = false;
            pageData.page = 1;
        } else {
            pageData.page = getNumberPage(post);
            for (var i = 0; i < pageData.countPage; i++) {
                pageData.pages[i] = {number: i + 1, active: (i + 1) === pageData.page};
            }
            pageData.first = pageData.page === 1;
            pageData.last = pageData.page === pageData.countPage;
        }
        var firstPost = (pageData.page - 1) * pageData.maxPost;
        var lastPost = firstPost + pageData.maxPost;
        data.post = post.slice(firstPost, lastPost);

        $("#page-post").load("templates.html #template-post-preview", function () {
            var template = document.getElementById('template-post-preview').innerHTML;
            var output = Mustache.render(template, data);
            $("#page-post").html(output);
        });
        $("#game-pagination-top").load("templates.html #template-pagination", function () {
            var template = document.getElementById('template-pagination').innerHTML;
            var output = Mustache.render(template, pageData);
            $("#game-pagination-top").html(output);
            $("#game-pagination-bottom").html(output);
        });
    };

    var createOnePost = function () {
        var id = getIdOpenPost();
        data.full = true;
        data.post = [];
        data.post[0] = post[id];
        data.post[0].id = id;
        var screens = data.post[0].screenshots;
        screens[0].active = true;
        $.each(screens, function (index, value) {
            value.id = index;
            return true;
        });
        $("#page-post").load("templates.html #template-post-preview", function () {
            var template = document.getElementById('template-post-preview').innerHTML;
            var output = Mustache.render(template, data);
            $("#page-post").html(output);
            $('.carousel').carousel(0);
        });
    };



    $(".menuitem").click(function () {
        $(".active").removeClass("active");
        $(this).addClass("active");
    });



    $.each(post, function (index, value) {
        value.id = index;
        return true;
    });

    var filterPage = $("#filter").val();
    if (filterPage !== "") {
        createContent(filterByField("title", filterPage, post));
    } else {
        var vars = getUrlVars();

        if (vars["post"])
            createOnePost();
        if (vars["home"] || vars["page"] || vars == "")
            createContent(post);
    }

    var filterByField = function (field, filter, data) {
        var resData = [];
        console.log("field=" + field);
        console.log("filter=" + filter);
        console.log("data.lenght=" + data.lenght);
        $.each(data, function (index, value) {
            if (value[field].toLowerCase().indexOf(filter) > -1) {
                resData.push(value);
                console.log("value.title=" + value.title);
            }
        });
        console.log("resData.lenght=" + resData.length);
        return resData;
    };

    $("#searchButton").click(function () {
        var filter = $("#searchField").val();
        console.log("Filter=" + filter);
        $("#filter").val(filter);
        //createContent(filterByField("title",filter,post));
    });

});


